function showdiv(obj) {

    var element=document.getElementById(obj);
    element.setAttribute("style","display: inline;");

}

function closediv(obj) {
    var element=document.getElementById(obj);

    element.setAttribute("style","display: none;");
}

function showloadingdialog(obj) {

    var element=document.getElementById(obj);
    element.setAttribute("style","display: inline;");

}
